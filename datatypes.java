
//                                                                  -: Data  Types :-

// Byte :- The byte data type can store whole numbers from -128 to 127. This can be used instead of int or othere integer types to save memory when you are certain that the value will be within -128 and 127.
// Example :-
// public class datatypes{
//     public static void main(String[]args){
//       byte myNum = 100;
//       System.out.println(myNum); 
//     }
// }

// Short :- The short data type can store whole numbers from -32768 to 32767.
// Example :-
// public class datatypes{
//     public static void main(String[]args){
//       short myNum = 5000;
//       System.out.println(myNum); 
//     }
// }

// Int :- The int data type can store whole numbers from -2147483648 to 2147483647. In general, and in our tutorial, the int data type is the preferred data type when we create variables with a numeric value.
// Example :-
// public class datatypes{
//     public static void main(String[]args){
//       int myNum = 100000;
//       System.out.println(myNum); 
//     }
// }

// Long :- The long data type can store whole numbers from -9223372036854775808 to 9223372036854775807. This is used when int is not large enough to stsore the value. Note that you should end the value with an "L".
// Example :-
// public class datatypes{
//     public static void main(String[]args){
//       long myNum = 15000000000L;
//       System.out.println(myNum); 
//     }
// }

// Floating Point Types :- You should use a floating point type whenever you need a number with a decimal, such as 9.99 or 3.14545. The float and double data types can store fractional numbers. Note that you should end the value with an "f" for floats and "d" for doubles.
// Example :-
// public class datatypes{
//     public static void main(String[]args){
//       float myNum = 5.65f;
//       System.out.println(myNum); 
//     }
// }

